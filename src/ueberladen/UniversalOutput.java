public class UniversalOutput {

    void printToScreen(int a){

        System.out.println(a);
    }


    void printToScreen(double a){

        System.out.println(a);
    }


    void printToScreen(char a){

        System.out.println(a);
    }

    void printToScreen(String a){

        System.out.println(a);
    }


}
